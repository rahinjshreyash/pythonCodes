# VOLUME OF CYLINDER
height = 30
radius = 15

volume = 3.14*radius*radius*height
print("The volume of cylinder with height 30 and radius 15 is",volume)

# AREA OF CYLINDER 
 
area = (2*3.14*radius*height) + (2*3.14*radius*radius)
print("The area of cylinder with height",height,"and radius ",radius,"is equal to",area)